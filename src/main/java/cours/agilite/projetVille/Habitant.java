package cours.agilite.projetVille;

public class Habitant {
	
	String nom;
	String prenom;
	String sexe;
	int age;
	String adresse;
	int nir; /*numéro de sécurité sociale (numero d'inscription au répertoire des personnes physiques.*/
	
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public String getPrenom() {
		return prenom;
	}
	public void setPrenom(String prenom) {
		this.prenom = prenom;
	}
	public String getSexe() {
		return sexe;
	}
	public void setSexe(String sexe) {
		this.sexe = sexe;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getAdresse() {
		return adresse;
	}
	public void setAdresse(String adresse) {
		this.adresse = adresse;
	}
	public int getNir() {
		return nir;
	}
	public void setNir(int nir) {
		this.nir = nir;
	}
	

}
